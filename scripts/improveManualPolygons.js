const fs = require('fs');
const path = require('path');

const OHM_DIR = path.join(__dirname, '../public/geo/borders/ohm');
const INDEX_PATH = path.join(__dirname, '../public/geo/borders/ohm_index.json');

let nextRid = 9100529;

function writeOhm(rid, coords, meta, geometryType = 'Polygon') {
  const outFile = `ohm_${rid}.geojson`;
  const geometry = geometryType === 'MultiPolygon'
    ? { type: 'MultiPolygon', coordinates: coords }
    : { type: 'Polygon', coordinates: [coords] };

  const ohmGJ = {
    type: 'FeatureCollection',
    features: [{
      type: 'Feature',
      properties: {
        ohm_id: `manual_${rid}`,
        name: meta.name_en,
        name_en: meta.name_en,
        name_ko: meta.name_ko,
        start_date: String(meta.start_date),
        end_date: String(meta.end_date),
        wikidata: meta.qid,
        admin_level: '2',
        source: 'manual_cl_improved',
        our_name_en: meta.name_en,
        our_name_ko: meta.name_ko,
        our_tier: meta.tier,
        border_precision: 2,
      },
      geometry,
    }]
  };
  fs.writeFileSync(path.join(OHM_DIR, outFile), JSON.stringify(ohmGJ));
  console.log(`  ✅ ${outFile} (${coords.flat(Infinity).length / 2} pts)`);
  return outFile;
}

const idx = JSON.parse(fs.readFileSync(INDEX_PATH, 'utf-8'));

function updateIndex(qid, newSnapshots) {
  const ei = idx.findIndex(e => e.qid === qid);
  if (ei >= 0) {
    idx[ei].snapshots = idx[ei].snapshots.filter(s => !(s.rid >= 9000000 && s.rid < 9100000));
    idx[ei].snapshots.push(...newSnapshots);
    idx[ei].snapshots.sort((a, b) => a.start - b.start);
    console.log(`  인덱스 업데이트: ${idx[ei].name_ko} (${qid})`);
    return true;
  }
  console.log(`  ⚠️ ${qid} 인덱스에 없음`);
  return false;
}

